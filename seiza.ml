(* 目的 : 誕生日 (月と日) を受け取ったら、星座を返す関数 *)
(* seiza : int -> int -> string *)
let seiza month day = 
    if (month = 3 && day >= 21) || (month = 4 && day <=19) then "おひつじ座"
    else if (month = 4 && day >= 20) || (month = 5 && day <=20) then "おうし座" 
    else if (month = 5 && day >= 21) || (month = 6 && day <= 21) then "ふたご座"
    else if (month = 6 && day >= 22) || (month = 7 && day <= 22) then "かに座"
    else if (month = 7 && day >= 23) || (month = 8 && day <= 22) then "しし座"
    else if (month = 8 && day >= 23) || (month = 9 && day <= 22) then "おとめ座"
    else if (month = 9 && day >= 23) || (month = 10 && day <= 23) then "てんびん座"
    else if (month = 10 && day >= 24) || (month = 11 && day <= 22) then "さそり座"
    else if (month = 11 && day >= 23) || (month = 12 && day <= 22) then "いて座"
    else if (month = 12 && day >= 23) || (month = 1 && day <= 19) then "やぎ座"
    else if (month = 1 && day >= 20) || (month = 2 && day <= 18) then "みずがめ座"
    else "うお座"

